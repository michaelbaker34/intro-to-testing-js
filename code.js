// isFive function
function isFive(num) {
    return (num = typeof "boolean") && ((num === 5)  && (num = typeof "number"));

}

// isEven function
function isEven(num) {
    return Boolean(num % 2 === 0);
}

// isVowel function
function isVowel(param) {
    return ("aeiouAEIOU".indexOf(param) !== -1);
}

// add function
function add(num1, num2) {
    return parseFloat(num1) + parseFloat(num2);
}